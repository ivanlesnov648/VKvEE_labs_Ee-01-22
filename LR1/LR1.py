"""
Пункт 2. Создать список и заполнить его элементами кортежа, увеличив четные в 3 раза,
а нечетные уменьшив на три. Вывести результат.
"""
# создаем пустой список
numbers_list = []

# перебираем все элементы кортежа
for num in numbers_tuple:
    # если число четное, увеличиваем его в 3 раза
    if num % 2 == 0:
        numbers_list.append(num * 3)
    else:
        # если число нечетное, уменьшаем его на 3
        numbers_list.append(num - 3)

# выводим результат
print("Пункт 2. Список:", numbers_list)


