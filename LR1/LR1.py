"""
Пункт 5. Написать функцию, на вход которой подается наименьшее, наибольшее
значение и шаг. По этим параметрам сгенерировать заполненный список. Посчитать
количество чисел кратных 6 и отнять от него минимальное число из всех чисел списка
возведенное в куб. Если полученный результат отрицательный необходимо зеркально
отобразить список, если положительный, то создать копию старого списка и добавить в его
начало полученное число. Итоговый список вернуть и вывести на экран.
"""
def process_numbers(min_value, max_value, step):
    # создаем список чисел от минимального до максимального значения
    numbers = list(range(min_value, max_value + 1, step))

    # оставляем только числа, кратные 6
    divisible_by_6 = list(filter(lambda x: x % 6 == 0, numbers))

    # считаем количество чисел, кратных 6
    count_div_6 = len(divisible_by_6)

    # находим минимальное число списка
    min_number = min(numbers)

    # вычисляем результат по условию
    result = count_div_6 - min_number ** 3

    # если результат отрицательный, переворачиваем список
    if result < 0:
        numbers.reverse()
        return numbers
    else:
        # если результат положительный, создаем копию списка
        new_numbers = numbers.copy()

        # добавляем результат в начало списка
        new_numbers.insert(0, result)
        return new_numbers


# вызываем функцию
result_list = process_numbers(1, 20, 2)

# выводим результат
print("Пункт 5. Итоговый список:", result_list)


"""
Пункт 6. Написать функцию, на вход которой подается строка, состоящая из
нескольких слов, разделенных пробелами. Записать в список все слова, начинающиеся с
маленькой буквы. Полученный результат отсортировать в порядке обратном алфавиту.
Итоговый список вернуть и вывести на экран. При выполнении задания использовать
функцию filter()
"""
def get_small_words(text):
    # разбиваем строку на слова
    words = text.split()

    # оставляем только слова, начинающиеся с маленькой буквы
    small_words = list(filter(lambda word: word[0].islower(), words))

    # сортируем в обратном алфавитном порядке
    small_words.sort(reverse=True)

    # возвращаем результат
    return small_words


# исходная строка
text = "Мама мыла Rama раму and окно"

# вызываем функцию
result_words = get_small_words(text)

# выводим результат
print("Пункт 6. Список слов:", result_words)


"""
Пункт 7. Написать функцию, которая будет возвращать истину, если аргумент
функции равен 3 в степени (больше) 1
"""
def is_power_of_three(number):
    # если число меньше 1, оно не подходит
    if number < 1:
        return False

    # пока число делится на 3 без остатка, делим его
    while number % 3 == 0:
        number = number // 3

    # если после деления получили 1, значит это степень 3
    return number == 1


# проверяем работу функции
print("Пункт 7:")
print("27 ->", is_power_of_three(27))
print("45 ->", is_power_of_three(45))
print("81 ->", is_power_of_three(81))
